class Rabbit{

String name ;
int age;
String color;
double weight;
String breed;

public String getName(){

	return name;
}

public int getAge(){

	return age;
}

public String getColor(){
	return color;
}

public double getWeight(){

	return weight;
}

public String getBreed(){

	return breed;
}
public Rabbit(String n, int a, String c, double w, String b){

name = n;
age = a;
color = c;
weight = w;
breed = b;

}
}